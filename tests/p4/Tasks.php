<?php

//This class is needed to start the session and open/close the browser
require_once __DIR__ . '/../wp-core/login.php';

class P4_Tasks extends P4_login {

  /**
   * @var \RemoteWebDriver
   */


  public function testTasks()
  {

   	//I log in
	try{
		$this->wpLogin();
	}catch(Exception $e){
		$this->fail('->Failed to log in, verify credentials and URL');
	}

	//Go to pages and create content  
   	$this->webDriver->wait(3);
	$pages = $this->webDriver->findElement(
	WebDriverBy::id("menu-pages"));
	$pages->click();
	try{
		$link = $this->webDriver->findElement(
		WebDriverBy::linkText("Add New")
		);
	}catch(Exception $e){
		$this->fail("->Could not find 'Add New' button in Pages overview");
	}
	$link->click();

	//Validate button to add blocks to page is present
	$this->assertContains(
	'Add Post Element',$this->webDriver->findElement(
	WebDriverBy::className('shortcake-add-post-element'))->getText()
	);


	//Enter title of page
	$field	= $this->webDriver->findElement(
	WebDriverBy::id('title-prompt-text')
	);
	$field->click();
	$this->webDriver->getKeyboard()->sendKeys('Test automated - Take Action tasks');

	//Click on button to add blocks
	$add = $this->webDriver->findElement(
	WebDriverBy::className("shortcake-add-post-element")
	);
	$add->click();

	//Validate blocks modal window is shown
	$this->assertContains(
	'Insert Post Element',$this->webDriver->findElement(
	WebDriverBy::className('media-frame-title'))->getText()
	);

	//Select TakeAction block
	try{
	$ta = $this->webDriver->findElement(
	WebDriverBy::cssSelector("li[data-shortcode='shortcake_tasks']")
	);
	$ta -> click();
	}catch(Exception $e){
		$this->fail("->Failed to select 'Take Action Tasks' post element");
	}

	//Validate tasks fields are visible
	try{
		//Fields task 1
		$this->webDriver->findElement(WebDriverBy::name("title_1"));
		$this->webDriver->findElement(WebDriverBy::name("description_1"));
		$this->webDriver->findElement(WebDriverBy::id("attachment_1"));
		$this->webDriver->findElement(WebDriverBy::name("button_text_1"));
		$this->webDriver->findElement(WebDriverBy::name("button_link_1"));
		// Fields task 2
		$this->webDriver->findElement(WebDriverBy::name("title_2"));
	    $this->webDriver->findElement(WebDriverBy::name("description_2"));
	    $this->webDriver->findElement(WebDriverBy::id("attachment_2"));
	    $this->webDriver->findElement(WebDriverBy::name("button_text_2"));
	    $this->webDriver->findElement(WebDriverBy::name("button_link_2"));
		//Fields task 3
		$this->webDriver->findElement(WebDriverBy::name("title_3"));
	    $this->webDriver->findElement(WebDriverBy::name("description_3"));
	    $this->webDriver->findElement(WebDriverBy::id("attachment_3"));
	    $this->webDriver->findElement(WebDriverBy::name("button_text_3"));
	    $this->webDriver->findElement(WebDriverBy::name("button_link_3"));
		//Fields 4
		$this->webDriver->findElement(WebDriverBy::name("title_4"));
	    $this->webDriver->findElement(WebDriverBy::name("description_4"));
	    $this->webDriver->findElement(WebDriverBy::id("attachment_4"));
	    $this->webDriver->findElement(WebDriverBy::name("button_text_4"));
	    $this->webDriver->findElement(WebDriverBy::name("button_link_4"));
	}catch(Exception $e){
		$this->fail("->Could not find tasks fields for 'Take Action Tasks' post element");
	}

	//---- Fill in title and description
	$ttitle = 'Take Action Tasks Test';
	$tdesc = 'This is content created by an automated test for testing take action tasks block';
	$field = $this->webDriver->findElement(
	WebDriverBy::name('tasks_title')
	);
	$field->click();
	$this->webDriver->getKeyboard()->sendKeys("$ttitle");
	
	$field = $this->webDriver->findElement(
	WebDriverBy::name('tasks_description')
	);
	$field->click();
	$this->webDriver->getKeyboard()->sendKeys("$tdesc");

	//--- FILL IN FIELDS FOR TASK 1
	$title1 = 'Test Task 1';
	$desc1 = 'This is the content for task 1 which is generated by an automated test';
	$field = $this->webDriver->findElement(
	WebDriverBy::name('title_1')
	);
	$field->click();
	$this->webDriver->getKeyboard()->sendKeys("$title1");
	$field = $this->webDriver->findElement(
	WebDriverBy::name('description_1')
	);
	$field->click();
 	$this->webDriver->getKeyboard()->sendKeys("$desc1");
	$this->webDriver->findElement(WebDriverBy::id("attachment_1"))->click();
	$tab = $this->webDriver->findElement(WebDriverBy::linkText('Media Library'));
	$tab->click();
	//Wait for media library to load
	$this->webDriver->wait(10, 1000)->until(
  	WebDriverExpectedCondition::presenceOfElementLocated(
	WebDriverBy::cssSelector('ul.attachments'))
	);
	$this->webDriver->manage()->timeouts()->implicitlyWait(10);
	//Select first image
	$srcfirstchild = explode("-",$this->webDriver->findElement(
		WebDriverBy::cssSelector("li.attachment:first-child img"))->getAttribute('src'));
	$srcfirstchild = $srcfirstchild[1];
	$img = $this->webDriver->findElement(WebDriverBy::cssSelector("li.attachment:first-child"));
	$img->click();
	//Get info needed to upload image 2
	$img2 = $this->webDriver->findElement(
		WebDriverBy::cssSelector("li.attachment:nth-child(2)"))->getAttribute('data-id');
	$src2child = explode("-",$this->webDriver->findElement(
		WebDriverBy::cssSelector("li.attachment:nth-child(2) img"))->getAttribute('src'));
	$src2child = $src2child[1];
	$this->webDriver->findElement(WebDriverBy::className("media-button-select"))->click();

	//--- FILL IN FIELDS FOR TASK 2
	$title2 = 'Test Task 2';
	$desc2 = 'This is the content for task 2 which is generated by an automated test';
	$field = $this->webDriver->findElement(
	WebDriverBy::name('title_2')
	);
	$field->click();
	$this->webDriver->getKeyboard()->sendKeys("$title2");
	$field = $this->webDriver->findElement(
	WebDriverBy::name('description_2')
	);
	$field->click();
 	$this->webDriver->getKeyboard()->sendKeys("$desc2");

	//Insert block
	try{
		$insert = $this->webDriver->findElement(
		WebDriverBy::className('media-button-insert')
		);
		$insert -> click();
	}catch(Exception $e){
		$this->fail('->Failed to insert element');
	}

	//Edit WYSIWYG text to add image 2
	$this->webDriver->findElement(WebDriverBy::id("content-html"))->click();
	$this->webDriver->findElement(WebDriverBy::id("content"))->click();
	$this->webDriver->getKeyboard()->pressKey(WebDriverKeys::ARROW_RIGHT);
	$this->webDriver->getKeyboard()->pressKey(WebDriverKeys::BACKSPACE);
	$this->webDriver->getKeyboard()->pressKey(WebDriverKeys::BACKSPACE);
	$this->webDriver->getKeyboard()->sendKeys("attachment_2=$img2 /]");

	//Publish content
	$this->webDriver->findElement(
	WebDriverBy::id('publish')
	)->click();
	
	//Wait to see successful message
	$this->webDriver->wait(10, 1000)->until(
		WebDriverExpectedCondition::visibilityOfElementLocated(
		WebDriverBy::id('message')));
	//Validate I see successful message
	try{
		$this->assertContains(
		'Page published',$this->webDriver->findElement(
		WebDriverBy::id('message'))->getText()
		);
	}catch(Exception $e){
		$this->fail('->Failed to publish content - no sucessful message after saving content');
	}
	//Wait for saved changes to load
	usleep(2000000);
	//Go to page to validate page contains added block
	$link = $this->webDriver->findElement(
		WebDriverBy::linkText('View page'));	
	$link->click();
	//If alert shows up asking to confirm leaving the page, confirm
	try{
		$this->webDriver->switchTo()->alert()->accept();
	}catch(Exception $e){}
	try{
		$this->webDriver->findElement(WebDriverBy::id('p4bks_tasks_container'));
		$ttitle_pg = $this->webDriver->findElement(
			WebDriverBy::cssSelector('#p4bks_tasks_container .container h3'))->getText();
		$tdesc_pg = $this->webDriver->findElement(
			WebDriverBy::cssSelector('#p4bks_tasks_container .container div.col-md-12'))->getText();
		$task1 = $this->webDriver->findElement(
			WebDriverBy::cssSelector('.col:nth-child(1) .step-number'))->getText();
		$tmp = explode("-",$this->webDriver->findElement(
			WebDriverBy::cssSelector('div.col:nth-child(1) .steps-information .steps-action img'))->getAttribute('src'));
		$srcimg = $tmp[1];
		$title1_pg = $this->webDriver->findElement(
			WebDriverBy::cssSelector('div.col:nth-child(1) .steps-information h5'))->getText();
		$desc1_pg = $this->webDriver->findElement(
			WebDriverBy::cssSelector('div.col:nth-child(1) .steps-information p'))->getText();
		$task2 = $this->webDriver->findElement(
			WebDriverBy::cssSelector('.col:nth-child(2) .step-number'))->getText();
		$srcimg2 = explode("-",$this->webDriver->findElement(
			WebDriverBy::cssSelector('div.col:nth-child(2) .steps-information .steps-action img'))->getAttribute('src'));
		$srcimg2 = $srcimg2[1];
		$title2_pg = $this->webDriver->findElement(
			WebDriverBy::cssSelector('div.col:nth-child(2) .steps-information h5'))->getText();
		$desc2_pg = $this->webDriver->findElement(
			WebDriverBy::cssSelector('div.col:nth-child(2) .steps-information p'))->getText();
	}catch(Exception $e){
		$this->fail('->Some of the content created is not displayed in front end page');
	}
	$this->assertEquals("$ttitle","$ttitle_pg");
	$this->assertEquals("$tdesc","$tdesc_pg");
	$this->assertEquals("1","$task1");
	$this->assertContains("$srcimg","$srcfirstchild");
	$this->assertEquals("$title1","$title1_pg");
	$this->assertEquals("$desc1","$desc1_pg");
	$this->assertEquals("2","$task2");
	$this->assertContains("$srcimg2","$src2child");
	$this->assertEquals("$title2","$title2_pg");
	$this->assertEquals("$desc2","$desc2_pg");
	// I log out after test
	$this->wpLogout();

	echo "\n-> Tasks block test PASSED";
  }


  protected function assertElementNotFound($by)
  {
	$this->webDriver->takeScreenshot('reports/screenshots/'.__CLASS__.'.png');
	$els = $this->webDriver->findElements($by);
	if (count($els)) {
		$this->fail("Unexpectedly element was found");
	}
	// increment assertion counter
	$this->assertTrue(true);

  }

}
